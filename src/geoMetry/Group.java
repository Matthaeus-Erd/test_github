package geoMetry;

import java.util.ArrayList;

public class Group {
	
	public ArrayList<Shapes> shapes;
	
	public Group() {
		this.shapes = new ArrayList<Shapes>();
	}

	public ArrayList<Shapes> getShapes() {
		return shapes;
	}

	public void setShapes(ArrayList<Shapes> shapes) {
		this.shapes = shapes;
	}
	
	
}